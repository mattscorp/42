#include "fdf.h"

int		check_error(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if ((str[i] >= '0' && str[i] <= '9') || str[i] == '-' || str[i] == ' '
				|| str[i] == '\n')
			i++;
		else
		{
			ft_putstr("WRONG FILE\n");
			exit(1);
		}
	}
	return (0);
}
