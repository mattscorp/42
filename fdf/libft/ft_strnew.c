#include "libft.h"

char	*ft_strnew(size_t size)
{
	unsigned int		i;
	char				*new;

	i = 0;
	if (!(new = (char*)malloc(sizeof(*new) * size + 1)))
		return (NULL);
	while (i < size)
	{
		new[i] = '\0';
		i++;
	}
	new[i] = '\0';
	return (new);
}
