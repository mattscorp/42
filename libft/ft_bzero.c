#include "libft.h"

void		ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*rtn;

	rtn = (char *)s;
	i = 0;
	while (i < n)
	{
		rtn[i] = '\0';
		i++;
	}
}
