#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*rtn;

	rtn = (char *)b;
	i = 0;
	while (i < len)
	{
		rtn[i] = c;
		i++;
	}
	return (b);
}
